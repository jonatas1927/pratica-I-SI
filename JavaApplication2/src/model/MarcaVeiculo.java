@Entity
public class MarcaVeiculo {

    @Id
    private int id;
    private String descricao;

    public MarcaVeiculo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

}
