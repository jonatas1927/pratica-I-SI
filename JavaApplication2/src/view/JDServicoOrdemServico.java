    public void updateTable() {
        model.setNumRows(0);
        for (ServicoOrdemServico m : DAO.listarPorOrdemServico(OrdSer.getId())) {;
            model.addRow(new String[]{
                "" + m.getId(),
                String.valueOf(m.getTempo()),
                String.valueOf(m.getValor()),
                m.getServico().getDescricao()
            });
        }
    }
