    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        SOS.setId(Integer.parseInt(txtCodigo.getText()));
        SOS.setOrdemServico(OrdSer);
        SOS.setServico(servico);
        SOS.setTempo(txtTempo.getText());
        SOS.setValor(Double.parseDouble(txtValor.getText()));
        if (SOS.getId() > 0) {
            DAO.update(SOS);
        } else {
            DAO.save(SOS);
        }
        updateTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        Object codigo = jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        SOS = DAO.findByID(Integer.parseInt(codigo.toString()), "ServicoOrdemServico");
        servico = SOS.getServico();
        txtCodigo.setText(SOS.getId() + "");
        txtTempo.setText(SOS.getTempo());
        txtValor.setText(SOS.getValor() + "");
    }//GEN-LAST:event_jButton3ActionPerformed

